    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {
            String cod = txtCodigo.getText();
            int cant = Integer.parseInt(txtCantidad.getText());
            String tipo = (String) cbTipo.getSelectedItem(); // "Entrada" o "Salida"

            boolean exito;
            if (tipo.equalsIgnoreCase("Entrada")) {
                exito = controlador.registrarEntrada(cod, cant);
            } else {
                exito = controlador.registrarSalida(cod, cant);
            }

            if (exito) {
                lblMensaje.setText("Movimiento registrado: " + tipo);
                lblMensaje.setForeground(Color.BLUE);
                txtCantidad.setText("");
            } else {
                lblMensaje.setText("Error: Stock insuficiente o producto no existe.");
                lblMensaje.setForeground(Color.RED);
            }
        } catch (NumberFormatException e) {
            lblMensaje.setText("Error: La cantidad debe ser un n√∫mero.");
            lblMensaje.setForeground(Color.RED);
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

